package oop.basic.constructor;

public class Employee {
	protected String name;
	protected int age;
	public Employee() {
		
	}
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
}